// what is OOP consept.

//Object-Oriented Programming (OOP) is a programming paradigm that uses "objects" to
// design software. These objects represent real-world entities and contain both data 
//(attributes) and methods (functions or procedures) that operate on the data. 
//OOP aims to increase the modularity, reusability, and maintainability of code by
// organizing it around these objects.
//
//Here are the key concepts of OOP:
//
//-------Class:----------
//
//A blueprint or template for creating objects.
//Defines a set of attributes and methods that the created objects will have.
//Object:
//
//------ instance of a class.----------
//Contains data (in the form of attributes) and behavior (in the form of methods).
//Encapsulation:
//
//Bundling the data and methods that operate on the data into a single unit or class.
//Restricting direct access to some of an object's components, which means that the 
//internal representation of an object can be hidden from the outside.
//------Abstraction:----------
//
//Simplifying complex reality by modeling classes appropriate to the problem.
//Hiding the complex implementation details and showing only the essential features of the object.
//Inheritance:
//
//Creating a new class (derived or child class) from an existing class (base or parent class).
//The new class inherits the attributes and methods of the existing class, allowing for code
// reusability and the creation of a hierarchical relationship between classes.


//-----Polymorphism:---------
//
//Allowing objects of different classes to be treated as objects of a common super class.
//The same method can behave differently on different classes, typically through method overriding (changing the implementation of a method in a derived class) or method overloading (having multiple methods with the same name but different parameters).

